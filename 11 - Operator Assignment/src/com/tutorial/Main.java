package com.tutorial;

public class Main {

    public static void main(String[] args){

        // Operator assignment
        // Operator aritmatika yang diakhiri tanda sama dengan

        // Assignment penjumlahan
        int a = 10;
        a += 5;
        System.out.println("10 += 5 --> " + a);

        // Assignment pengurangan
        int b = 15;
        b -= 5;
        System.out.println("15 -= 5 --> " + b);

        // Assignment perkalian
        int c = 20;
        c *= 5;
        System.out.println("20 *= 5 --> " + c);

        // Assignment pembagian
        int d = 15;
        d /= 3;
        System.out.println("15 /= 3 --> " + d);

        // Assignment modulus
        int e = 10;
        e %= 3;
        System.out.println("10 %= 3 --> " + e);

    }
}
